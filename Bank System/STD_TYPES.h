#ifndef  STD_TYPES_H
#define  STD_TYPES_H
typedef unsigned char uint8;				//unsigned char		
typedef unsigned short int uint16;			//short int			
typedef unsigned long int uint32;			//unsigned long int	
typedef signed char sint8;					//signed char
typedef signed short int sint16;			//signed short int
typedef signed long int sint32;			//signed long int
typedef float float32;					//float
typedef double float64;					//double
#endif
